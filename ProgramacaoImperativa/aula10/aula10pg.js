// Exercício lista de compras

let nomesProdutos = ["banana", "maça", "pera"];

console.log("Funçao join junta todos os elementos de um array");
console.log(nomesProdutos.join());

console.log("Função pop elimina o ultimo elemento da array e retorna ele");
console.log(nomesProdutos.pop());

console.log(nomesProdutos);

console.log("Função push adiciona um elemento ao final do array e retorna o tamanho da array");
console.log(nomesProdutos.push("carne"));

console.log(nomesProdutos);
console.log("Função shift elimina o primeiro elemento da array e retorna ele");
console.log(nomesProdutos.shift());

console.log(nomesProdutos);

console.log("Função unshift adiciona um novo elemento no começo da array e retorna o tamanho da array");
console.log(nomesProdutos.unshift("Peixe"));
console.log(nomesProdutos);
